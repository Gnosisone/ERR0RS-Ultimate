class HTMLReportEngine:
    """
    Generates a full professional HTML pentest report.
    One call to .generate() produces the complete report string.
    """

    def __init__(self, config: ReportConfig = None):
        self.config = config or ReportConfig()

    def generate(self, session: EngagementSession) -> str:
        """
        Main entry point. Returns a complete HTML document string.
        """
        findings = session.all_findings

        # Sort findings by severity (Critical first)
        findings.sort(key=lambda f: f.severity.priority_order)

        # Apply severity filter if configured
        if self.config.severity_filter:
            min_priority = self.config.severity_filter.priority_order
            findings = [f for f in findings if f.severity.priority_order <= min_priority]

        summary = session.finding_summary

        html_parts = [
            self._render_doctype_and_head(),
            self._render_css(),
            self._render_cover_page(session),
            self._render_executive_summary(session, summary) if self.config.include_executive else "",
            self._render_findings_dashboard(summary, session),
            self._render_findings_detail(findings),
            self._render_remediation_roadmap(findings) if self.config.include_remediation else "",
            self._render_timeline(session) if self.config.include_timeline else "",
            self._render_appendix(session) if self.config.include_raw_output else "",
            self._render_footer(),
            "</body></html>",
        ]
        return "\n".join(html_parts)

    # -----------------------------------------------------------------------
    # DOCUMENT STRUCTURE
    # -----------------------------------------------------------------------

    def _render_doctype_and_head(self) -> str:
        return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8" />
<meta name="viewport" content="width=device-width, initial-scale=1.0" />
<title>{self.config.company_name} — Security Assessment Report</title>
</head>"""

    def _render_footer(self) -> str:
        return f"""
<div class="report-footer">
    <p>Generated by <strong>ERR0RS ULTIMATE</strong> — AI-Powered Security Assessment Framework</p>
    <p>Report generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S UTC')}</p>
    <p class="confidential">⚠️ CONFIDENTIAL — This report contains sensitive security information.
    Distribute only to authorized personnel.</p>
</div>"""

