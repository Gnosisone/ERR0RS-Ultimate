"""
ERR0RS ULTIMATE - Professional HTML Report Engine
=====================================================
This generates the actual pentest reports. Full professional quality.

ARCHITECTURE:
-------------
1. ReportEngine takes an EngagementSession + ReportConfig
2. It pulls all findings, sorts by severity
3. It generates executive summary (non-technical)
4. It generates technical findings (per-finding detail)
5. It attaches education from the knowledge base
6. It renders everything into a single HTML file

The HTML is self-contained: all CSS is inlined. No external deps.
You can open it in any browser, print it, or convert to PDF.

TEACHING NOTE ON REPORTS:
-------------------------
Professional pentest reports have a specific structure that clients
expect. This engine follows that structure exactly:
  - Cover page with engagement details
  - Executive summary (risk overview, business impact)
  - Findings summary dashboard (counts by severity)
  - Detailed findings (each with proof, remediation, education)
  - Remediation roadmap (prioritized action list)
  - Appendix (raw tool output, methodology notes)
"""

